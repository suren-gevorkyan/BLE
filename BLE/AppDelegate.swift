extension Dictionary {
    func toString(options: JSONSerialization.WritingOptions = []) -> String? {
        do {
            let data = try JSONSerialization.data(withJSONObject: self, options: options)
            return String(data: data, encoding: .utf8)
        } catch {
            return nil
        }
    }
